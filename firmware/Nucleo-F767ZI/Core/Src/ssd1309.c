#define SSD1309_FONT_FIRST 0x20
#define SSD1309_FONT_LAST  0x7E
#define SSD1309_FONT_COLS  5
#define SSD1309_FONT_ADVANCE 6

static HAL_StatusTypeDef ssd1309_write_cmd(ssd1309_t *p, const uint8_t *cmds, uint16_t len)
{
	return HAL_I2C_Mem_Write(
		p->port,
		(uint16_t)(p->address << 1),
		SSD1309_I2C_CONTROL_CMD,
		I2C_MEMADD_SIZE_8BIT,
		(uint8_t *)cmds,
		len,
		SSD1309_I2C_TIMEOUT_MS
	);
}

static HAL_StatusTypeDef ssd1309_write_data(ssd1309_t *p, const uint8_t *data, uint16_t len)
{
	return HAL_I2C_Mem_Write(
		p->port,
		(uint16_t)(p->address << 1),
		SSD1309_I2C_CONTROL_DATA,
		I2C_MEMADD_SIZE_8BIT,
		(uint8_t *)data,
		len,
		SSD1309_I2C_TIMEOUT_MS
	);
}

/**
 * ssd1309_write_pages
 *
 * Writes `data` (width * (height/8) bytes, page-major) to GDDRAM in
 * page-addressing mode: for each page, reset the column address to 0 and
 * write that page's `width` bytes.
 */
static HAL_StatusTypeDef ssd1309_write_pages(ssd1309_t *p, const uint8_t *data)
{
	HAL_StatusTypeDef status;
	uint8_t num_pages = (uint8_t)(p->height / 8);

	for (uint8_t page = 0; page < num_pages; page++) {
		uint8_t page_cmds[] = {
			(uint8_t)(SSD1309_CMD_SET_PAGE_START + page),
			SSD1309_CMD_SET_LOW_COLUMN,
			SSD1309_CMD_SET_HIGH_COLUMN,
		};
		status = ssd1309_write_cmd(p, page_cmds, sizeof(page_cmds));
		if (status != HAL_OK) {
			return status;
		}

		status = ssd1309_write_data(p, &data[(uint16_t)page * p->width], (uint16_t)p->width);
		if (status != HAL_OK) {
			return status;
		}
	}

	return HAL_OK;
}

void ssd1309_init(
	ssd1309_t *p,
	I2C_HandleTypeDef *in_port,
	uint16_t in_address,
	int16_t in_height,
	int16_t in_width)
{
	p->port = in_port;
	p->address = in_address;
	p->height = (in_height < 0) ? 64 : in_height;
	p->width = (in_width < 0) ? 128 : in_width;
	memset(p->buffer, 0x00, sizeof(p->buffer));
	p->dirty = false;
}

HAL_StatusTypeDef ssd1309_bringup(ssd1309_t *p)
{
	HAL_StatusTypeDef status;

	uint8_t init_cmds[] = {
		SSD1309_CMD_DISPLAY_OFF,
		SSD1309_CMD_SET_CLOCK_DIV, 0x80,
		SSD1309_CMD_SET_MULTIPLEX, (uint8_t)(p->height - 1),
		SSD1309_CMD_SET_DISPLAY_OFFSET, 0x00,
		SSD1309_CMD_SET_START_LINE,
		SSD1309_CMD_SEGMENT_REMAP,
		SSD1309_CMD_COM_SCAN_DEC,
		SSD1309_CMD_SET_COM_PINS, 0x12,
		SSD1309_CMD_SET_CONTRAST, 0x8F,
		SSD1309_CMD_SET_PRECHARGE, 0xF1,
		SSD1309_CMD_SET_VCOMH, 0x40,
		SSD1309_CMD_ENTIRE_DISPLAY_RESUME,
		SSD1309_CMD_NORMAL_DISPLAY,
		SSD1309_CMD_DEACTIVATE_SCROLL,
	};

	status = ssd1309_write_cmd(p, init_cmds, sizeof(init_cmds));
	if (status != HAL_OK) {
		return status;
	}

	uint8_t mem_addr_mode[] = {
		SSD1309_CMD_SET_MEMORY_ADDR_MODE, SSD1309_MEMORY_ADDR_MODE_PAGE,
	};
	status = ssd1309_write_cmd(p, mem_addr_mode, sizeof(mem_addr_mode));
	if (status != HAL_OK) {
		return status;
	}

	/* All-on test pattern, using the framebuffer as scratch space */
	size_t fb_size = (size_t)p->width * (size_t)(p->height / 8);
	memset(p->buffer, 0xFF, fb_size);
	status = ssd1309_write_pages(p, p->buffer);
	if (status != HAL_OK) {
		return status;
	}
	memset(p->buffer, 0x00, fb_size);
	p->dirty = false;

	uint8_t display_on[] = { SSD1309_CMD_DISPLAY_ON };
	return ssd1309_write_cmd(p, display_on, sizeof(display_on));
}

void ssd1309_clear(ssd1309_t *p)
{
	size_t fb_size = (size_t)p->width * (size_t)(p->height / 8);
	memset(p->buffer, 0x00, fb_size);
	p->dirty = true;
}

void ssd1309_fill(ssd1309_t *p, uint8_t color)
{
	size_t fb_size = (size_t)p->width * (size_t)(p->height / 8);
	memset(p->buffer, (color == SSD1309_COLOR_ON) ? 0xFF : 0x00, fb_size);
	p->dirty = true;
}

void ssd1309_set_pixel(ssd1309_t *p, int16_t x, int16_t y, uint8_t color)
{
	if (x < 0 || x >= p->width || y < 0 || y >= p->height) {
		return;
	}

	uint16_t index = (uint16_t)((y / 8) * p->width + x);
	uint8_t mask = (uint8_t)(1u << (y % 8));
	uint8_t old = p->buffer[index] & mask;
	uint8_t new = (color == SSD1309_COLOR_ON) ? mask : 0;

	if (old != new) {
		p->buffer[index] = (uint8_t)((p->buffer[index] & ~mask) | new);
		p->dirty = true;
	}
}

uint8_t ssd1309_get_pixel(ssd1309_t *p, int16_t x, int16_t y)
{
	if (x < 0 || x >= p->width || y < 0 || y >= p->height) {
		return SSD1309_COLOR_OFF;
	}

	uint16_t index = (uint16_t)((y / 8) * p->width + x);
	uint8_t mask = (uint8_t)(1u << (y % 8));

	return (p->buffer[index] & mask) ? SSD1309_COLOR_ON : SSD1309_COLOR_OFF;
}

HAL_StatusTypeDef ssd1309_flush(ssd1309_t *p)
{
	HAL_StatusTypeDef status;

	if (!p->dirty) {
		return HAL_OK;
	}

	status = ssd1309_write_pages(p, p->buffer);
	if (status == HAL_OK) {
		p->dirty = false;
	}

	return status;
}

void ssd1309_draw_char(ssd1309_t *p, int16_t x, int16_t y, char c, uint8_t color)
{
	if (c < SSD1309_FONT_FIRST || c > SSD1309_FONT_LAST) {
		return;
	}

	const uint8_t *glyph = font5x7[(uint8_t)c - SSD1309_FONT_FIRST];

	for (int16_t col = 0; col < SSD1309_FONT_COLS; col++) {
		uint8_t bits = glyph[col];
		for (int16_t row = 0; row < 7; row++) {
			if (bits & (1u << row)) {
				ssd1309_set_pixel(p, x + col, y + row, color);
			}
		}
	}
}

void ssd1309_draw_string(ssd1309_t *p, int16_t x, int16_t y, const char *str, uint8_t color)
{
	while (*str != '\0') {
		ssd1309_draw_char(p, x, y, *str, color);
		x += SSD1309_FONT_ADVANCE;
		str++;
	}
}
